// booleans & comparison operator

//booleans
// true, false

let num1 = 7;
let num2 = "7";

console.log(num1 < num2);

// == vs ===
console.log(num1 == num2); // check only value not data type
console.log(num1 === num2); // check value as well as data type

// != vs !==
console.log(num1 != num2); // check only value not data type
console.log(num1 !== num2); // check value as well as data type
