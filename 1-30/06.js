let firstName = "Ashish";

console.log(firstName[0]); //print first character

console.log(firstName.length); // length of string

let length = firstName.length;
let lastIndex = length - 1;

console.log(firstName[lastIndex]); //print last character

console.log(firstName.toUpperCase()); //print in uppercase
console.log(firstName.toLowerCase()); //print in lowercase
console.log(firstName.charAt(0)); //print first character
console.log(firstName.charAt(lastIndex)); //print last character
console.log(firstName.indexOf("h")); //print index of character
console.log(firstName.lastIndexOf("h")); //print last index of character
console.log(firstName.includes("S")); //print true or false
console.log(firstName.includes("hish")); //print true or false
console.log(firstName.indexOf("h")); //print index of character
